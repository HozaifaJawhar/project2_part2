class NewVolunteer {
  final String name;
  final String imageUrl;
  final String address;
  final String date;
  final String gender;
  final String email;
  final String phone;

  final String department;

  NewVolunteer({
    required this.name,
    required this.imageUrl,
    required this.address,
    required this.date,
    required this.gender,
    required this.email,
    required this.phone,
    required this.department,
  });
}
